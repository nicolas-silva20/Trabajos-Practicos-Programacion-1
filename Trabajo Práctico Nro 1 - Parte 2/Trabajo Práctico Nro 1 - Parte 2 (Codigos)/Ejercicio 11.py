#Se solicita el ingreso de dos números
numero1 = float(input("Ingrese el primer número: "))
numero2 = float(input("Ingrese el segundo número: "))

#Se calcula la diferencia entre los dos números
diferencia = numero1 - numero2

#Se calcula el valor absoluto de la diferencia
distancia = abs(diferencia)

#Se imprime la distancia
print(f"La distancia entre los números es: {distancia}")